beforeEach(function () {
    config([
        'ai.providers.openrouter.key' => 'test-key',
        'ai.providers.groq.key' => 'test-key',
        'text-generation.version' => 'test-v1',
        'text-generation.roles.classifier.provider' => 'openrouter',
        'text-generation.roles.classifier.model' => 'openrouter/auto',
