test('BYOK text routing follows ordered models records actual usage and emits no global AI events', function () {
    $user = User::factory()->create();
    AiAccountSetting::factory()->for($user)->create(['byok_enabled' => true]);
    $credential = AiProviderCredential::factory()->for($user)->create([
        'secret' => 'sk-or-v1-isolated-user-secret-key',
        'fingerprint' => str_repeat('b', 64),
    ]);
    AiModelPreference::factory()->for($user)->create([
        'purpose' => AiModelPurpose::ShortText,
        'mode' => AiModelMode::Custom,
        'model_ids' => ['vendor/first-model', 'vendor/second-model'],
    ]);
    Event::fake([PromptingAgent::class, AgentPrompted::class, RequestSending::class, ResponseReceived::class]);
    $http = app(ByokHttpFactory::class);
    $http->fakeSequence()
        ->push(['error' => ['message' => 'first failed']], 500)
        ->push([
            'model' => 'vendor/second-model',
            'choices' => [[
                'message' => ['content' => 'classified'],
                'finish_reason' => 'stop',
            ]],
            'usage' => ['prompt_tokens' => 11, 'completion_tokens' => 7, 'cost' => 0.004321],
        ]);

    $result = app(TextRoleManager::class)->generate(TextGenerationRequest::make(
        TextGenerationRole::Classifier,
        'Classify this company.',
        user: $user,
    ));

    $audits = AiOperationAudit::query()->where('account_id', $user->id)->orderBy('id')->get();
    $models = collect($http->recorded())->map(fn (array $entry): string => (string) $entry[0]->data()['model'])->all();

    expect($models)->toBe(['vendor/first-model', 'vendor/second-model'])
        ->and($result->model)->toBe('vendor/second-model')
        ->and($audits->pluck('event')->all())->toBe([
            AiAuditEvent::Started,
            AiAuditEvent::Failed,
            AiAuditEvent::Started,
            AiAuditEvent::Succeeded,
        ])
        ->and($audits->last()->credential_fingerprint)->toBe($credential->fingerprint)
        ->and($audits->last()->input_tokens)->toBe(11)
        ->and($audits->last()->output_tokens)->toBe(7)
        ->and((float) $audits->last()->cost_usd)->toBe(0.004321);

    Event::assertNotDispatched(PromptingAgent::class);
    Event::assertNotDispatched(AgentPrompted::class);
    Event::assertNotDispatched(RequestSending::class);
    Event::assertNotDispatched(ResponseReceived::class);
});

test('isolated BYOK providers parse OpenRouter usage without global AI or HTTP events', function () {
    Event::fake([PromptingAgent::class, AgentPrompted::class, RequestSending::class, ResponseReceived::class]);
    app(ByokHttpFactory::class)->fake([
        'openrouter.ai/api/v1/chat/completions' => Http::response([
            'model' => 'vendor/model',
            'choices' => [[
                'message' => ['content' => 'ok'],
                'finish_reason' => 'stop',
            ]],
            'usage' => ['prompt_tokens' => 2, 'completion_tokens' => 1, 'cost' => 0.001],
        ]),
    ]);
    $agent = new TextRoleAgent('Classify.');
    $provider = app(ByokOpenRouterProviderFactory::class)->make('sk-or-v1-isolated-test-key');

    $response = $provider->prompt(new AgentPrompt($agent, 'Prompt', [], $provider, 'vendor/model', 10));

    expect($response->text)->toBe('ok');
    Event::assertNotDispatched(PromptingAgent::class);
    Event::assertNotDispatched(AgentPrompted::class);
    Event::assertNotDispatched(RequestSending::class);
    Event::assertNotDispatched(ResponseReceived::class);
});

test('isolated BYOK provider connection failures emit no global HTTP events', function () {
    Event::fake([RequestSending::class, ConnectionFailed::class]);
    app(ByokHttpFactory::class)->fake([
        'openrouter.ai/api/v1/chat/completions' => ByokHttpFactory::failedConnection(),
    ]);
    $agent = new TextRoleAgent('Classify.');
    $provider = app(ByokOpenRouterProviderFactory::class)->make('sk-or-v1-isolated-test-key');

    expect(fn () => $provider->prompt(new AgentPrompt($agent, 'Prompt', [], $provider, 'vendor/model', 10)))
        ->toThrow(ConnectionException::class);

    Event::assertNotDispatched(RequestSending::class);
    Event::assertNotDispatched(ConnectionFailed::class);
});

test('known BYOK text models honor account budgets using conservative estimates', function () {
    $user = User::factory()->create();
    AiAccountSetting::factory()->for($user)->create([
        'byok_enabled' => true,
        'monthly_usd_limit' => 0.20,
        'per_operation_usd_limit' => 0.11,
    ]);
    AiProviderCredential::factory()->for($user)->create(['secret' => 'sk-or-v1-budget-key']);
    AiModelPreference::factory()->for($user)->create([
        'purpose' => AiModelPurpose::ShortText,
        'mode' => AiModelMode::Custom,
        'model_ids' => ['openrouter/auto'],
    ]);
    $http = app(ByokHttpFactory::class);
    $http->fake([
        'openrouter.ai/api/v1/chat/completions' => Http::response([
            'model' => 'openrouter/auto',
            'choices' => [[
                'message' => ['content' => 'classified'],
                'finish_reason' => 'stop',
            ]],
            'usage' => ['prompt_tokens' => 10, 'completion_tokens' => 5, 'cost' => 0.03],
        ]),
    ]);

    $result = app(TextRoleManager::class)->generate(TextGenerationRequest::make(
        TextGenerationRole::Classifier,
        'Classify this company.',
        user: $user,
    ));

    expect($result->text)->toBe('classified')
        ->and((float) AiOperationAudit::query()->where('event', AiAuditEvent::Started->value)->sole()->cost_usd)->toBe(0.10)
        ->and((float) AiOperationAudit::query()->where('event', AiAuditEvent::Succeeded->value)->sole()->cost_usd)->toBe(0.03);

    $user->aiAccountSetting()->update(['per_operation_usd_limit' => 0.05]);

    expect(fn () => app(TextRoleManager::class)->generate(TextGenerationRequest::make(
        TextGenerationRole::Classifier,
        'Classify another company.',
        user: $user,
    )))->toThrow(PaidAiUnavailable::class);

    expect($http->recorded())->toHaveCount(1);
});
