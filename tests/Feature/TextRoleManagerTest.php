        'text-generation.roles.classifier.provider_options' => [
            'provider' => [
                'max_price' => ['prompt' => 1.0, 'completion' => 2.0],
            ],
        ],
        'text-generation.roles.classifier.fallbacks' => [
            ['provider' => 'groq', 'model' => 'llama-test', 'timeout' => 7],
        ],
    ]);
