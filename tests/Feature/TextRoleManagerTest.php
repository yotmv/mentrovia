test('text roles resolve provider model and fallback config', function () {
    $profile = app(TextRoleManager::class)->resolve(TextGenerationRole::Classifier);

    expect($profile->role)->toBe(TextGenerationRole::Classifier)
        ->and($profile->configVersion)->toBe('test-v1')
        ->and($profile->candidates)->toBe([
            [
                'provider' => 'openrouter',
                'model' => 'openrouter/auto',
                'timeout' => 12,
                'providerOptions' => ['provider' => ['max_price' => ['prompt' => 1.0, 'completion' => 2.0]]],
            ],
            [
                'provider' => 'groq',
                'model' => 'llama-test',
                'timeout' => 7,
                'providerOptions' => [],
            ],
        ]);
});

test('missing role config fails before prompting', function () {
    config(['text-generation.roles.classifier.model' => null]);

    app(TextRoleManager::class)->resolve(TextGenerationRole::Classifier);
})->throws(TextGenerationRoleException::class, 'model');

test('role generation falls back to the next configured provider', function () {
    Log::spy();

    TextRoleAgent::fake(function (string $prompt, $attachments, $provider, string $model) {
        if ($provider->name() === 'openrouter') {
            throw new RuntimeException('private provider response content');
        }

        return "Generated by {$provider->name()} using {$model}: {$prompt}";
    });

    $result = app(TextRoleManager::class)->generate(TextGenerationRequest::make(
        TextGenerationRole::Classifier,
        'Classify this company.',
        ['industry' => 'countertops'],
        User::factory()->create(),
    ));

    expect($result->text)->toContain('Generated by groq using llama-test')
        ->and($result->provider)->toBe('groq')
        ->and($result->model)->toBe('llama-test')
        ->and($result->configVersion)->toBe('test-v1');

    TextRoleAgent::assertPrompted(fn ($prompt): bool => str_contains($prompt->prompt, '"industry": "countertops"'));

    Log::shouldHaveReceived('warning')
        ->once()
        ->with('Text generation role candidate failed; trying fallback if available.', [
            'role' => TextGenerationRole::Classifier->value,
            'provider' => 'openrouter',
            'model' => 'openrouter/auto',
            'exception_class' => RuntimeException::class,
        ]);
});

test('provider failure details are not chained into the public role exception', function () {
    TextRoleAgent::fake(fn () => throw new RuntimeException('private provider response content'));

    try {
        app(TextRoleManager::class)->generate(TextGenerationRequest::make(
            TextGenerationRole::Classifier,
            'Classify this company.',
            user: User::factory()->create(),
        ));

        test()->fail('Expected every configured provider to fail.');
    } catch (TextGenerationRoleException $exception) {
        expect($exception->getMessage())->not->toContain('private provider response content')
            ->and($exception->getPrevious())->toBeNull();
    }
});

